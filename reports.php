    var swiper = new Swiper(".mySwiper", {
        slidesPerView: 3,
        spaceBetween: 30,
        loop: true,
        pagination: {
            el: ".swiper-pagination",
            clickable: true,
        },
        navigation: {
            nextEl: ".swiper-button-next",
            prevEl: ".swiper-button-prev",
        },
        on: {
            slideChange: function () {
                updateSlideText();
            },
        },
    });

    function updateSlideText() {
        var activeSlideIndex = swiper.realIndex;
        var slideTexts = [
            "Text for Slide 1",
            "Text for Slide 2",
            // Add text for other slides here
        ];
        var slideTextElement = document.querySelector(".slide-text");
        slideTextElement.textContent = slideTexts[activeSlideIndex];
    }
